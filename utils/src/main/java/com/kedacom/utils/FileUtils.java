    public static void deleteDir(String filePath) {
        File dir = new File(filePath);
        if (dir == null || !dir.exists() || !dir.isDirectory())
            return;

        for (File file : dir.listFiles()) {
            if (file.isFile())
                file.delete();
            else if (file.isDirectory())
                deleteDir(filePath);
        }
        dir.delete();
    }

